function getPreferredLang() {
  const saved = localStorage.getItem('site_lang');
  if (saved === 'kz' || saved === 'ru') return saved;
  return (navigator.language || '').toLowerCase().startsWith('kk') ? 'kz' : 'ru';
}

function applyLanguage(lang) {
  const dict = DICTIONARY[lang] || DICTIONARY.ru;
  document.documentElement.lang = lang === 'kz' ? 'kk' : 'ru';
  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    if (!key || !dict[key]) return;
    el.textContent = dict[key];
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (!key || !dict[key]) return;
    el.setAttribute('placeholder', dict[key]);
  });
  document.querySelectorAll('[data-i18n-aria]').forEach((el) => {
    const key = el.getAttribute('data-i18n-aria');
    if (!key || !dict[key]) return;
    el.setAttribute('aria-label', dict[key]);
  });
  document.querySelectorAll('[data-set-lang]').forEach((btn) => {
    btn.classList.toggle('active', btn.getAttribute('data-set-lang') === lang);
  });
  localStorage.setItem('site_lang', lang);
}

document.querySelectorAll('[data-set-lang]').forEach((button) => {
  button.addEventListener('click', () => applyLanguage(button.getAttribute('data-set-lang') || 'ru'));
});

applyLanguage(getPreferredLang());
